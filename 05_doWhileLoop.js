//print number 1 to 10 using while loop
//initialization=0 condition= i<=10 update= i++
var i = 1; // initialization
while (i <= 10) {
  console.log(i);
  i++;
}

//print table of 5
var i = 5;
while (i <= 50) {
  console.log(i);
  i = i + 5;
}

//print 1 to 10 using do while loop
//initialization=0 condition= i<=10 update= i++

var i = 1;
do {
  console.log(i);
  i++;
} while (i <= 10);

//print table of 6 using do while loop
//initialization=0 condition= i<=10 update= i++

var i = 6;
do {
  console.log(i);
  i = i + 6;
} while (i <= 60);
