console.log(`============2.1 Value Swap===========`);
var sweety = "Sweety";
var cutie = "Cutie";

console.log(`Before Swap values are `);
console.log(sweety, cutie);
var temp = "Sweety";
sweety = cutie;
cutie = temp;
console.log(`After Swap values are `);
console.log(sweety, cutie);

